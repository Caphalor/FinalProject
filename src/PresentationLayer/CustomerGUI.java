    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased

        String searchCusName = txtSearch.getText(); //get CustomerName from the txtSearch textfield and pass to the searchCusName variable
        ArrayList<Customer> cusList = new Customer().searchCus(searchCusName); //pass the CustomerName to the searchCustomer sub program and get entire items from the database depends on the CustomerName
        Object obj[][] = new Object[cusList.size()][5]; //create new object for the table
        int x = 0; //create new variable
        for (Customer cusObj : cusList) { //use for each loop to display entire details
            obj[x][0] = cusObj.getCusID(); //for first column to insert CusID
            obj[x][1] = cusObj.getCusName(); //for second column to insert CusName
            obj[x][2] = cusObj.getAddress(); //for third column to insert Address
            obj[x][3] = cusObj.getTeleNo(); //for forth column to insert TelePhoneNo
            x++; //increate loop iteration

        }

        tblCus.setModel(new javax.swing.table.DefaultTableModel( //create new table models using swing
                obj, //declare the object here
                new Object[]{ //create new object
                    "Customer ID", "Customer Name", "Address", "Telephone No" //give the column names
                }));
    }//GEN-LAST:event_txtSearchKeyReleased

