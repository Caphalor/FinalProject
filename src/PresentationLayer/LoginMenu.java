    public static String now(String dateFormat) { //this sub program used to get current date and time
        Calendar cal = Calendar.getInstance(); //get the current date and time
        SimpleDateFormat sdf = new SimpleDateFormat(dateFormat); //make the date and the time format
        return sdf.format(cal.getTime()); //return the date and time
    }
