    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed

        String username = txtUsername.getText(); //pass the txtUsername textfield to variable
        Login itemObjUserName = new Login().getLoginDetails(username); //pass the variable to getLoginDetails sub program in MegaVideo class 
        try {
            if ((itemObjUserName.getmUsername()).equalsIgnoreCase(txtUsername.getText()) && (itemObjUserName.getmPassword()).equalsIgnoreCase(txtPassword.getText())) {//in this if condition check the username and the passwords are equlas to the database username and the password
                OrderGUI OrdGUI = new OrderGUI(); //call OrderGUI JFrame class
                OrdGUI.setVisible(true); //open the OrderGUI class
                this.dispose(); //dispose the running gui program
            }
        } catch (Exception ex) { //if any errors occur it will catch the errors
            String Error = "Incorrect Username or Password"; //display the error message
            JOptionPane.showMessageDialog(this, Error);
        }

    }//GEN-LAST:event_btnLoginActionPerformed

