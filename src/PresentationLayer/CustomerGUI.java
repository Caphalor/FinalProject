    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        Customer cusObj = new Customer(); //create object for Customer class
        cusObj.setCusID(txtCusID.getText()); //get CusID from txtMovieID textfield
        cusObj.setCusName(txtCusName.getText()); //get CusName from txtMovieID textfield
        cusObj.setAddress(txtAddress.getText()); //get Address from txtMovieID textfield
        cusObj.setTeleNo(s2I(txtTeleNo.getText())); //get Email from txtMovieID textfield

        boolean result = cusObj.addCus(cusObj); //add to the addCustomer sub program
        if (result) { //if the result equals true
            fillCusId(); //call the fillTablecus subprogram
            fillTable(); //call the fillCusId subprogram
            String Message = "Customer Successfully Added to the DataBase"; //display the message
            String Result = "Message";
            jOptionPane1.showMessageDialog(this, Message, Result, WIDTH, null);
        } else {
            String ERROR = "Error in Adding to the database"; //display the error message
            String Result = "ERROR";
            jOptionPane1.showMessageDialog(this, ERROR, Result, WIDTH, null);
        }
    }//GEN-LAST:event_btnAddActionPerformed

