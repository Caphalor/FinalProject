    public void fillCusId() { //this sub program used to get entire CustomerIDs from the database
        cmbCusID.removeAllItems(); //remove all current cmbCusID combobox
        ArrayList<Customer> cusList = new Customer().getAllCus(); //get entire CustomerIDs from the database and save it from the ArrayList
        for (Customer cusObj : cusList) { //use for each loop to display entire details
            cmbCusID.addItem(cusObj.getCusID()); //add CustomerIDs to the cmbCusID combobox
        }
    }

    public void fillTable() { //this sub program used to get entire Customer details from the database and display from the table

        ArrayList<Customer> cusList = new Customer().getAllCusDB(); //get entire items from the database save to the ArrayList
        Object obj[][] = new Object[cusList.size()][5]; //create new object for the table
        int x = 0; //create new variable
        for (Customer cusObj : cusList) { //use for each loop to display entire details
            obj[x][0] = cusObj.getCusID(); //for first column to insert CusID
            obj[x][1] = cusObj.getCusName(); //for second column to insert CusName
            obj[x][2] = cusObj.getAddress(); //for third column to insert Address
            obj[x][3] = cusObj.getTeleNo(); //for forth column to insert Email
            x++; //increate loop iteration
        }


        tblCus.setModel(new javax.swing.table.DefaultTableModel( //create new table models using swing
                obj, //declare the object here
                new Object[]{ //create new object
                    "Customer ID", "Customer Name", "Address", "Telephone No" //give the column names
                }));
    }

